package Increment_Decrement_Operator_Programs;

public class Program_4 {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(++x);
        System.out.println(x++);
        System.out.println(x);

        //  Exp and Result Table
        // Line No. |  a |  Exp
        // 5 | 5 | NA
        // 6 | 6 | 1+5 = 6
        // 7 | 6 | 1+6 = 7
        // 8 | 7 | NA
    }
}
