package Test_19th_Jun;

public class Arithmetic_operations {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        float z = 3.2f;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%z);
    }
}
