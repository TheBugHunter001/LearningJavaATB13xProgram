package Test_19th_Jun;

public class Comparison_with_logical_operators {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;
        System.out.println(a==b); // Should be false as a is not equal to b
        System.out.println(a!=b); // Should be true
        System.out.println(a<b); // Should be true as a is less than b
        System.out.println(a>b); // Should be false as a is less than b
        System.out.println(a<=b); // Should be true as a is less than b
        System.out.println(a>=b); // Should be false as a is less than b

    }
}
